package com.young.entry;

public class Student {
	private String className; //班级
	private String stuName;  //姓名
	private String stuNum;  //学号
	private String IDCard;  //生分证号
	private String sex;  //性别
	private String ethnic; //民族
	private String college; //学院
	private String major; //专业
	private String year;  //学制
	private String politicalStatus; //政治面貌
	private String birthDay; //出生日期
	private String enterScholl; //入校日期
	private String leftScholl;  //离校日期
	public String getClassName() {
		return className;
	}
	public void setClassName(String className) {
		this.className = className;
	}
	public String getStuName() {
		return stuName;
	}
	public void setStuName(String stuName) {
		this.stuName = stuName;
	}
	public String getIDCard() {
		return IDCard;
	}
	public void setIDCard(String iDCard) {
		IDCard = iDCard;
	}
	public String getSex() {
		return sex;
	}
	public void setSex(String sex) {
		this.sex = sex;
	}
	public String getEthnic() {
		return ethnic;
	}
	public void setEthnic(String ethnic) {
		this.ethnic = ethnic;
	}
	public String getCollege() {
		return college;
	}
	public void setCollege(String college) {
		this.college = college;
	}
	public String getMajor() {
		return major;
	}
	public void setMajor(String major) {
		this.major = major;
	}
	public String getYear() {
		return year;
	}
	public void setYear(String year) {
		this.year = year;
	}
	public String getPoliticalStatus() {
		return politicalStatus;
	}
	public void setPoliticalStatus(String politicalStatus) {
		this.politicalStatus = politicalStatus;
	}
	public String getBirthDay() {
		return birthDay;
	}
	public void setBirthDay(String birthDay) {
		this.birthDay = birthDay;
	}
	public String getEnterScholl() {
		return enterScholl;
	}
	public void setEnterScholl(String enterScholl) {
		this.enterScholl = enterScholl;
	}
	public String getLeftScholl() {
		return leftScholl;
	}
	public void setLeftScholl(String leftScholl) {
		this.leftScholl = leftScholl;
	}
	public String getStuNum() {
		return stuNum;
	}
	public void setStuNum(String stuNum) {
		this.stuNum = stuNum;
	}

	

}
