package com.young.entry;

public class ScoreInfo {
	private String stuId; //学号
	private String averageGradePoint; // 总绩点
	private String totalGradePoint;  // 平均绩点
	private String name;  //姓名
	private String content; // 内容，包括班级，学号等
	public String getStuId() {
		return stuId;
	}
	public void setStuId(String stuId) {
		this.stuId = stuId;
	}
	
	public String getAverageGradePoint() {
		return averageGradePoint;
	}
	public void setAverageGradePoint(String averageGradePoint) {
		this.averageGradePoint = averageGradePoint;
	}
	public String getTotalGradePoint() {
		return totalGradePoint;
	}
	public void setTotalGradePoint(String totalGradePoint) {
		this.totalGradePoint = totalGradePoint;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}
	
	
	
	

}
